	<div class="box">
		<div class="t">主要特点</div>
		<ul>
			<li>简单：
				<p>没有庞大的文件结构，没有复杂的类属关系，没有夸张的函数列表，一切简简单单，明明白白，程序的本质就是化繁为简嘛。</p>
			</li>
			<li>易用：
				<p>几乎不需要学习，只要你会使用PHP和OOP，因为，我们只有7个核心类、6个系统函数和2个配置文件，而且，大部分情况下，你只需要使用1个方法。</p>
			</li>
			<li>灵活：
				<p>我们拥有你想象不到的智慧：你只需要把网页简单的放进去，它就会正确的呈现出来；你只需要配置一条验证规则，它就会自动完成从客户端到服务器的验证；你只需要把SQL语句写进文件里，它就会恰当的去执行。</p>
			</li>
		</ul>
